         <!-- VISION ET MISSION DE L'AIDES -->
    <div class="modal modal-warning fade" id="climat">
          <div class="modal-dialog">
            <div class="modal-content">
              <div class="modal-header">
                <button type="button" class="close" data-dismiss="modal" aria-label="Close">
                  <span aria-hidden="true">&times;</span></button>
                <h4 class="modal-title"><center> VISION, MANDAT/MISSION ET PRINCIPES ET VALEURS  DE L'AIDES</center></h4>
              </div>
              <div class="modal-body">
                <ul>
                <li>VISION DE L'AIDES</li>
                <p>« une société où l’injustice et la pauvreté n’ont plus de place »&hellip;</p>
                <li>MANDAT ET MISSION DE L'AIDES</li>
                <p>AIDES a pour mission d’appuyer les mécanismes et stratégies d’action en faveur des victimes de toute forme d’injustice socio-économique&hellip;</p>
                <li>PRINCIPES ET VALEURS D'AIDES</li>
                <p>Guidée par les principes humanitaires d’indépendance, de neutralité, d’impartialité, d’humanisme, du respect des cultures locales et du « Do no harm », s’emploie en collaboration avec les autres acteurs à ce que les personnes touchées par les crises humanitaires soient humainement traitées et continuent à vivre en toute dignité.
                Les interventions d’AIDES se caractérisent également par le respect des engagements au Code de Conduite (Préparé conjointement par la Fédération des Sociétés de la Croix-Rouge et du Croissant-Rouge et le CICR), à la Charte Humanitaire et aux Normes Minimales pour les Interventions lors de Catastrophes (Sphère), au Partenariat International pour la redevabilité Humanitaire (HAP), au Code de conduite en terme de gestion et appui du personnel humanitaire (People in Aid) et à la Déclaration sur la Protection contre l’exploitation sexuelle et les abus par la charte.
                Le principe spécifique à AIDES est de se considérer comme des apprentis continuels au sein d’un cercle où le partage est le bienvenu au point de toujours chercher à aller de l’avant en apprenant des autres.<br> 
                Les valeurs fondamentales d’AIDES sont :
                <ol>L’honnêteté</ol>
                <ol>l’intégrité</ol>
